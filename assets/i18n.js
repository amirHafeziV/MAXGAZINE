const RTL_LANGS = ['fa','ar'];
const OG_LOCALE = {en:'en_US',fa:'fa_IR',ar:'ar_AR',tr:'tr_TR'};

function _merge(lang){
  const base = Object.assign({}, window.I[lang]);
  if(window.PAGE_I && window.PAGE_I[lang]) Object.assign(base, window.PAGE_I[lang]);
  return base;
}
function _setMeta(sel, val){ const el=document.querySelector(sel); if(el) el.setAttribute('content', val); }

/* Guess the visitor's language from their geographic location (IANA timezone)
   and, as a softer signal, their browser locale preferences. Only used as the
   default when the visitor has made no manual choice (no stored preference,
   no ?lang= override). A manual pick from the footer always wins thereafter. */
function detectLang(){
  const supported = Object.keys(window.I); // en, fa, ar, tr
  try{
    const tz = (Intl.DateTimeFormat().resolvedOptions().timeZone)||'';
    const TZ_LANG = { 'Asia/Tehran':'fa', 'Europe/Istanbul':'tr', 'Asia/Istanbul':'tr' };
    if(TZ_LANG[tz]) return TZ_LANG[tz];
    const AR_TZ = ['Asia/Riyadh','Asia/Dubai','Asia/Qatar','Asia/Kuwait','Asia/Bahrain',
      'Asia/Baghdad','Asia/Amman','Asia/Beirut','Asia/Damascus','Asia/Aden','Asia/Muscat',
      'Asia/Gaza','Asia/Hebron','Africa/Cairo','Africa/Tripoli','Africa/Tunis','Africa/Algiers',
      'Africa/Casablanca','Africa/El_Aaiun','Africa/Khartoum','Africa/Nouakchott','Africa/Djibouti','Africa/Mogadishu'];
    if(AR_TZ.includes(tz)) return 'ar';
  }catch(e){}
  const prefs = navigator.languages || [navigator.language || ''];
  for(const p of prefs){
    const code = (p||'').toLowerCase().split('-')[0];
    if(supported.indexOf(code) !== -1) return code;
  }
  return 'en';
}
window.detectLang = detectLang;

function setLang(l, persist){
  if(persist===undefined) persist=true;
  if(!window.I[l]) l='en';
  const dict = _merge(l);
  document.querySelectorAll('[data-i]').forEach(el=>{
    const k = el.getAttribute('data-i');
    if(dict[k]!==undefined) el.textContent = dict[k];
  });
  const dir = RTL_LANGS.includes(l) ? 'rtl' : 'ltr';
  document.documentElement.dir = dir;
  document.documentElement.lang = l;
  if(document.body) document.body.dir = dir;
  // multi-paragraph translated blocks: show only the block matching the active language
  document.querySelectorAll('[data-lang-block]').forEach(el=>{
    el.hidden = el.getAttribute('data-lang-block') !== l;
  });
  // localized title/description via per-page meta keys (pm_title / pm_desc)
  if(dict.pm_title){ document.title = dict.pm_title; _setMeta('meta[property="og:title"]',dict.pm_title); _setMeta('meta[name="twitter:title"]',dict.pm_title); }
  if(dict.pm_desc){ _setMeta('meta[name="description"]',dict.pm_desc); _setMeta('meta[property="og:description"]',dict.pm_desc); _setMeta('meta[name="twitter:description"]',dict.pm_desc); }
  _setMeta('meta[property="og:locale"]', OG_LOCALE[l]||'en_US');
  document.querySelectorAll('.lang button').forEach(b=>{
    const on = b.dataset.lang===l; b.classList.toggle('active',on); b.setAttribute('aria-pressed',on);
  });
  const url = new URL(location.href);
  if(l==='en') url.searchParams.delete('lang'); else url.searchParams.set('lang',l);
  history.replaceState(null,'',url);
  if(persist){ try{ localStorage.setItem('mg_lang', l); }catch(e){} }
}
window.setLang = setLang;

function initLang(){
  // Generated article pages live under /en|fa|ar|tr/ — their language is fixed
  // by the path; never override it with the stored site-wide preference.
  const fixed = (location.pathname.match(/\/(en|fa|ar|tr)\//)||[])[1];
  if(fixed){ setLang(fixed); return; }
  const fromUrl = new URLSearchParams(location.search).get('lang');
  if(window.I[fromUrl]){ setLang(fromUrl); return; }
  let stored=null; try{ stored=localStorage.getItem('mg_lang'); }catch(e){}
  if(window.I[stored]){ setLang(stored); return; }
  // No manual choice yet → default to the visitor's location, without persisting
  // (so it stays location-driven until they pick a language from the footer).
  setLang(detectLang(), false);
}
window.initLang = initLang;
